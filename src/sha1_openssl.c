int cws_sha1(const void *in, size_t len, void *out)
{
    int rv          = -1;
    EVP_MD_CTX *ctx = EVP_MD_CTX_create();

    if (ctx
        && (1 == EVP_DigestInit_ex(ctx, EVP_sha1(), NULL))
        && (1 == EVP_DigestUpdate(ctx, in, len))
        && (1 == EVP_DigestFinal_ex(ctx, out, NULL)))
    {
        rv = 0;
    }

    if (ctx) {
        EVP_MD_CTX_destroy(ctx);
    }

    return rv;
}


